package Learn_Basic;

public class StringF {
    public static void main(String[] args) {
        System.out.printf("This is a string, %s \n", "NICE");
        System.out.printf("String %10d \n", 10);
        System.out.printf("String %-10d \n", 5000);
        System.out.printf("String %-10d \n", 10000);
        System.out.printf("String %10d \n", 10000000);
        System.out.printf("String %3d \n", 1000000000);
        System.out.printf("String %.2f \n", 45.546534);
    }
}
